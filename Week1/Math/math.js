const sum = (a, b) => {
    return a + b
};

const subtract = (c, d) => {
    return c - d 
};

const multiplication = (e, f) => {
    return e * f
};

const divison = (g, h) => {
    return g / h
};

module.exports = {
    sum: sum,
    subtract: subtract,
    multiplication: multiplication,
    division: divison
};